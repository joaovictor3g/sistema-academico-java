    private  boolean validacaoAluno(){
        try {   
            FileInputStream arquivo = new FileInputStream("AlunoLogineSenha.txt");
            InputStreamReader input = new InputStreamReader(arquivo);
            BufferedReader br = new BufferedReader(input);

            String linha = null;
            do{
                linha = br.readLine();
                
                if(linha != null){
                    String[] palavras = linha.split(";");
                    for(int i = 0; i < palavras.length; i++){
                        if(jLogin.getText().equals(palavras[i]) && new String(jSenha.getPassword()).equals(palavras[i+1])){
                            alunoIn.ajustarValor(Integer.parseInt(palavras[2]));
                            //historico.setIDAluno(Integer.parseInt(palavras[2]));
                            return true;
                        }
                    }
                }
            }while(linha != null);
            br.close();
            input.close();
            arquivo.close();
            
        } catch (FileNotFoundException e) {
            System.out.println("Arquivo não aberto, HERE!!!!!!");
            
        } catch (IOException ex) {
            Logger.getLogger(MenuPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }
    
    
