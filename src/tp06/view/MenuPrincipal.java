    private boolean validacaoProfessor(){
        boolean retorno = false;
        try {
            FileInputStream arquivo = new FileInputStream("RegistrosdeProfessores.txt");
            InputStreamReader input = new InputStreamReader(arquivo);
            BufferedReader br = new BufferedReader(input);

            String linha = null;
            do{
                linha = br.readLine();
                
                if(linha != null){
                    String[] palavras = linha.split(";");
                    for(int i = 0; i < palavras.length; i++){
                        if(jLogin.getText().equals(palavras[i]) && new String(jSenha.getPassword()).equals(palavras[i+1])){
                            viewPro.setIDProfessor(Integer.parseInt(palavras[3]));
                            return true;
                        }
                        
                    }
                }
            }while(linha != null);
            br.close();
            input.close();
            arquivo.close();
            
        } catch (Exception e) {
            System.out.println("Arquivo não aberto");
        }
        return false;
    }
