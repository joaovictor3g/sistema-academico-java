    public boolean verificarProfessor(){
        //preencherTabelas();
        try{
            FileInputStream abrir = new FileInputStream("RegistrosdeProfessores.txt");
            InputStreamReader input = new InputStreamReader(abrir);
            BufferedReader br = new BufferedReader(input);
            
            String linha = null;
            do{
                linha = br.readLine();
                if(linha != null){
                    String[] palavras = linha.split(";");
                    for(int i = 0; i < palavras.length; i++){
                        if(jTextID.getText().equals(palavras[i])){
                            //preencherTabelas();
                            jNome.setText(palavras[i-3]);
                            jdisc.setText(palavras[i+4]);
                            preencherTabelas();
                            return true;
                        }
                    }
                }
                
            }while(linha != null);
            
        }catch(Exception e){
            
        }
        return false;
    }
