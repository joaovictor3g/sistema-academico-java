    private void pegarCargaHoraria(){
        try{
            FileInputStream abrir = new FileInputStream("Disciplinas.txt");
            InputStreamReader input = new InputStreamReader(abrir);
            BufferedReader br = new BufferedReader(input);
            
            String linha;
            String[] palavras;
            
            while((linha = br.readLine()) != null){
                if(linha != null){
                    palavras= linha.split(";");
                    
                    for(int i = 0; i < palavras.length; i++){
                        if(jdisc.getText().equals(palavras[i]))
                            jTextCarga.setText(palavras[1]);
                        
                    }
                    
                }
                
                
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            
        }
    }
