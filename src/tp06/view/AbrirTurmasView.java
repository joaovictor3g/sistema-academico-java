    //Pegando as informações passadas nos JtextsFields
    public boolean aberturaDeTurma(){
        //preencherTabelas();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        if(!verificarHorarioAluno()){
            try{
                FileWriter criarArquivo = new FileWriter("TurmasAbertas.txt", true);
                PrintWriter gravarArquivo = new PrintWriter(criarArquivo);


                gravarArquivo.print(jNome.getText() + ";" + jTextID.getText() + ";"+ jCodigo.getText() +"A"+ ";" +jCursoBox.getSelectedItem() + ";" + jInstBox.getSelectedItem() + ";" + jdisc.getText() + ";" + jTextCarga.getText() +";"+ jCBHorario.getSelectedItem()+ ";" + jCBsemana.getSelectedItem()+ ";" +"\n");
                int x = jTable1.getRowCount();
                for(int i = 0; i < x + 1; i++){
                    for(int j = 0; j < x + 1; j++){
                        if(modelo.getValueAt(j, 0) == null)
                            break;
                        //Código da turma
                        gravarArquivo.print(jCodigo.getText() + ";");
                        //Informações do aluno Nome e CPF
                        gravarArquivo.print(modelo.getValueAt(j, 0) + ";");
                        gravarArquivo.print(modelo.getValueAt(j, 1) + ";");
                        gravarArquivo.print(jdisc.getText() + ";" + jNome.getText() + ";" + jCBHorario.getSelectedItem() +";" + jCBsemana.getSelectedItem()+ ";"+"\n");
                    }
                    break;
                }
                gravarArquivo.close();
                criarArquivo.close();
                
                return true;
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e + " aqui");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Alunos já cadastrado nesse horário");
        }
        
        return false;
    }
    //Metodo para verificar se aluno ja tem aula no horario definido
    private boolean verificarHorarioAluno(){
        try{
            FileInputStream abrir = new FileInputStream("TurmasAbertas.txt");
            InputStreamReader input = new InputStreamReader(abrir);
            BufferedReader br = new BufferedReader(input);
            
            String percorrer_linhas;
            String[] percorrer_palavras;
            
            while((percorrer_linhas = br.readLine()) != null){
                if(percorrer_linhas != null){
                    percorrer_palavras = percorrer_linhas.split(";");
                    
                    for(int i = 0; i <= percorrer_palavras.length - (percorrer_palavras.length - 1); i++){
                        if(jCBHorario.getSelectedItem().equals(percorrer_palavras[i]) && jCBsemana.getSelectedItem().equals(percorrer_palavras[8])){
                            return true;
                        }
                    }
                    
                }
                
            }
            
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            
        }
        return false;
    }
